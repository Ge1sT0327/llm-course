import json, os

COURSE_ROOT = r"G:\CLAUDE CODE_PROJECT\SHIXI\course"

def make_notebook(cells):
    """创建标准 Jupyter notebook JSON"""
    return {
        "cells": cells,
        "metadata": {
            "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
            "language_info": {"name": "python", "version": "3.10.0"}
        },
        "nbformat": 4, "nbformat_minor": 5
    }

def md(source):
    return {"cell_type": "markdown", "metadata": {}, "source": source if isinstance(source, list) else [source]}

def code(source):
    return {"cell_type": "code", "metadata": {}, "outputs": [],
            "source": source if isinstance(source, list) else [source]}

def save_notebook(dirname, filename, cells):
    path = os.path.join(COURSE_ROOT, dirname, filename)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(make_notebook(cells), f, ensure_ascii=False, indent=1)
    print(f"OK: {dirname}/{filename}")

