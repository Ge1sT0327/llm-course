        '        """简单评分: 长度合理 + 关键词匹配"""\n',
        '        s = 0\n',
        '        if 50 < len(response) < 800: s += 40\n',
        '        elif len(response) > 0: s += 20\n',
        '        if expected:\n',
        '            hits = sum(1 for w in expected.split() if w in response)\n',
        '            s += min(40, hits * 10)\n',
        '        if any(w in response for w in ["根据", "来源", "知识库", "文档"]): s += 20\n',
        '        return min(100, s)\n',
