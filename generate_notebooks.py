        '        avg_latency = sum(r["latency_ms"] for r in self.results) / len(self.results)\n',
        '        return f"评估 {len(self.results)} 个案例 | 均分: {avg_score:.0f} | 均延迟: {avg_latency:.0f}ms"\n',
